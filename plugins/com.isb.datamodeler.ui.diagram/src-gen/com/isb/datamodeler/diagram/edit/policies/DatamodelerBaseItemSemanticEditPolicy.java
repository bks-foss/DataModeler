	protected void addDestroyViewsCommand(ICompositeCommand cmd, View view) {
		assert view.getEAnnotation("Shortcut") == null; //$NON-NLS-1$

		List<View> viewsToRemove = new ArrayList<View>();

		//Eliminamos los Shortcuts y Views de todos los diagramas
		for (EStructuralFeature.Setting setting : EcoreUtil.UsageCrossReferencer
				.find(view.getElement(), getEditingDomain().getResourceSet())) {
			if (!(setting.getEObject() instanceof View)
					|| setting.getEObject() == view
					|| viewsToRemove.contains(setting.getEObject()))
				continue;
			viewsToRemove.add((View) setting.getEObject());
		}

		for (View viewToRemove : viewsToRemove)
			cmd.add(new DeleteCommand(getEditingDomain(), viewToRemove));
	}

